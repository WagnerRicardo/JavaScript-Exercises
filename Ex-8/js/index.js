//IntParse
var var1 = prompt('Digite um número: ')
var var2 = prompt('Digite outro número: ')

document.write('<h2>ParseInt</h2>')

var1 = parseInt(var1)
var2 = parseInt(var2)

document.write(var1 + var2)

console.log(var1)
console.log(var2)
